-- Add created_by column to distinguish coach-assigned vs trainee-owned plans
ALTER TABLE public.workout_plans ADD COLUMN IF NOT EXISTS created_by TEXT DEFAULT 'coach' CHECK (created_by IN ('coach', 'trainee'));
ALTER TABLE public.diet_plans ADD COLUMN IF NOT EXISTS created_by TEXT DEFAULT 'coach' CHECK (created_by IN ('coach', 'trainee'));

-- Allow trainees to fully manage their own plans
DROP POLICY IF EXISTS "Trainees manage own workout plans" ON public.workout_plans;
CREATE POLICY "Trainees manage own workout plans" ON public.workout_plans FOR ALL
  USING (auth.uid() = trainee_id AND created_by = 'trainee')
  WITH CHECK (auth.uid() = trainee_id AND created_by = 'trainee');

DROP POLICY IF EXISTS "Trainees manage own diet plans" ON public.diet_plans;
CREATE POLICY "Trainees manage own diet plans" ON public.diet_plans FOR ALL
  USING (auth.uid() = trainee_id AND created_by = 'trainee')
  WITH CHECK (auth.uid() = trainee_id AND created_by = 'trainee');
